    assertTrue(areAnagrams("Protectionism", "Cite no imports"));
    assertTrue(areAnagrams("Protectionism", "Nice to imports"));
    assertTrue(areAnagrams("A domesticated animal", "Docile, as a man tamed it"));
    assertTrue(areAnagrams("The Railroad Train", "Hi! I Rattle and Roar"));
    assertTrue(areAnagrams("The Hilton", "Hint: Hotel"));
