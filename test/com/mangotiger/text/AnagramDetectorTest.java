    assertTrue(areAnagrams("The country side", "No City Dust Here"));
    assertTrue(areAnagrams("Evangelist", "Evil's Agent"));
    assertTrue(areAnagrams("Astronomers", "Moon starers"));
    assertTrue(areAnagrams("Astronomers", "No more stars"));
    assertTrue(areAnagrams("Postmaster", "Stamp Store"));
    assertTrue(areAnagrams("A telescope", "To see place"));
    assertTrue(areAnagrams("The eyes", "They see"));
    assertTrue(areAnagrams("The ears", "Hear set"));
    assertTrue(areAnagrams("The cockroach", "Cook, catch her"));
    assertTrue(areAnagrams("Waitress", "A stew, Sir?"));
    assertTrue(areAnagrams("The centenarians", "I can hear ten \"tens\""));
    assertTrue(areAnagrams("Desperation", "A rope ends it"));
    assertTrue(areAnagrams("I run to escape", "A persecution"));
    assertTrue(areAnagrams("The Morse Code", "Here Come Dots"));
    assertTrue(areAnagrams("The Meaning of Life", "The fine game of nil"));
    assertTrue(areAnagrams("Slot Machines", "Cash Lost in'em"));
